package OOPS;

public class ExampleStaticMethods {


    //static

    public static int num = 100;

    public static int squareOfNumber(int number)
    {
        int result = number* number;
        return result;
    }

    //non static method

    public int productOfNumbers(int a, int b)
    {
        int result = a * b;
        return result;
    }


}
